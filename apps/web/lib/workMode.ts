export function getWorkMode(phase: CyclePhase, checkIn?: DailyCheckIn): WorkMode {
  if (checkIn?.pain?.level === "strong" || checkIn?.energy?.value === "exhausted") {
    return {
      ...phaseWorkModes.menstruation,
      label: "Беречь ресурс",
      title: "Снизить нагрузку сегодня",
      body: "Есть сильный сигнал от тела. Лучше перенести тяжёлые встречи, выбрать минимум обязательного и сделать больше пауз.",
      bestFor: ["минимум задач", "перенос встреч", "восстановление"],
      avoid: "терпеть сильную боль и работать как обычно",
      pause: "Пауза каждые 45–60 минут, вода и тёплая еда.",
      messageTemplate: "Сегодня мне физически тяжело, поэтому я перенесу часть встреч и сфокусируюсь на самом срочном.",
    };
  }

  if (checkIn?.energy?.value === "low" || checkIn?.sleep?.quality === "bad" || checkIn?.sleep?.quality === "insomnia") {
    return {
      ...phaseWorkModes.luteal,
      label: "Низкая энергия",
      title: "Работать мягче",
      body: "Энергия ниже обычного. Лучше выбрать спокойные задачи, убрать лишние переключения и оставить запас времени.",
      bestFor: ["рутина", "проверка", "короткие задачи"],
      avoid: "важные решения на усталости",
      pause: "25–50 минут работы, затем 5–10 минут паузы.",
      messageTemplate: "Сегодня работаю в более спокойном темпе: зафиксирую главное и вернусь с апдейтом по готовности.",
    };
  }

  if (checkIn?.mood?.value === "anxiety" || checkIn?.symptomLog?.anxiety) {
    return {
      ...phaseWorkModes.luteal,
      label: "Меньше давления",
      title: "День без лишнего шума",
      body: "Если тревожно, помогает ясный список и меньше спонтанных встреч. Сначала самое понятное, потом сложное.",
      bestFor: ["чек-лист", "письменные апдейты", "простые шаги"],
      avoid: "внезапные созвоны и спорные обсуждения без повестки",
      pause: "Перед встречей 2 минуты дыхания и короткая повестка.",
      messageTemplate: "Чтобы не потерять детали, давай зафиксируем вопросы письменно, а я отвечу по пунктам.",
    };
  }

  return phaseWorkModes[phase];
}
